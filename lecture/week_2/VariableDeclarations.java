public class VariableDeclarations 
{
    public static void main(String[] args) 
    {
        int num; // declares a variable named num
                 // which holds values of type int (integers)

        String str; // declares a variable named str
                    // which holds values of type String
                    // (strings of characters - text)

        int otherNum; // another variable, named otherNum.
                      // variable names are case-sensitive,
                      // so otherNum is different from othernum

        String s1; // variable named s1
                   // while variables cannot begin with numbers, they
                   // may contain numbers
    }
}
